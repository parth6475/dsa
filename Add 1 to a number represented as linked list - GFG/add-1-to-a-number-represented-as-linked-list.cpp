class Solution
{
    public:
    int addCarry(Node *head){
        if(!head)   return 1;
        int res=head->data+addCarry(head->next);
        head->data=res%10;
        return res/10;
    }
    Node* addOne(Node *head) 
    {
        if(!head)   return NULL;
        int carry=addCarry(head);
        if(carry){
            Node* newnode=new Node(carry);
            newnode->next=head;
            return newnode;
        }
        return head;
    }
};

